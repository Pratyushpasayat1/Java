package pattern;

public class a {
public static void main(String[] args) {
	int n=10;
	for
	(int i=1;i<=n;i++) {
		for(int s=1;s<=n;s++) {
			
				System.out.print("  ");
			}
		for(int j=1;j<=1*2-1;j++) {
			System.out.print("*");
		}
			
			
		
		System.out.println();
	}}
	
}

